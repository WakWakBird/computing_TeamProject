def recommendedMenu(userInputIngredients):
    recommendedMenuList = []
    for menu, info in menuArchive.items():
        ingredients = info["재료"]
        if all(ingredient in ingredients for ingredient in userInputIngredients):
            recommendedMenuList.append((menu, ingredients, info["레시피"]))

    return recommendedMenuList

